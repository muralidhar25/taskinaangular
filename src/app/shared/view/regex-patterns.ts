export const patterns = {
  get password() {
    return "^(?=.*\\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[a-zA-Z]).{6,}$";
  },
  get phone() {
    // return "^\\(\\d\\d\\d\\)\\s\\d\\d\\d\-\\d\\d\\d\\d$";
    // return "^\+?(\([0-9]{3}\)|[0-9]{3})(-. )?[0-9]{3}(-. )?[0-9]{4}$";
    return `^\d?(?:(?:[\+]?(?:[\d]{1,3}(?:[ ]+|[\-.])))?[(]?(?:[\d]{3})[\-/)]?(?:[ ]+)?)?(?:[a-zA-Z2-9]
      [a-zA-Z0-9 \-.]{6,})(?:(?:[ ]+|[xX]|(i:ext[\.]?)){1,2}(?:[\d]{1,5}))?$`;
  },
  get email() {
    return "^[A-Za-z0-9]+[_A-Za-z0-9\\.-]*[A-Za-z0-9]+@[A-Za-z0-9-]+(\\.[A-Za-z0-9-]+)*(\\.[A-Za-z]{2,4})$";
  }
};
